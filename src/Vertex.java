
public class Vertex {
	String name;
	int value;
	public Vertex(String name,int value) {
		this.value=value;
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
}
